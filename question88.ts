/* The Math.round() function is perfect for rounding decimals to the closest whole number. It looks at the decimal
 part and decides whether to round up or down.*/
  

 function roundingDecimal(num: number): number{
    return Math.round(num);
 }
 console.log(roundingDecimal(5.6));
 console.log(roundingDecimal(3.2));
 
 
